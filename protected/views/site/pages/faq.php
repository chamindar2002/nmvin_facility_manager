<h1>Frequently asked questions (FAQ's)</h1>

<h3>Can I buy items from <?php echo Yii::app()->name; ?> without creating an account?</h3>
Yes you can. <br>
We simply do not want our customers to enforce creating and signing into accounts to purchase items from our site.<br>
Our main objective is to provide our clients a hassle free quick and smooth checkout shopping experience.<br>

<h3>Can I view my orders and order statuses without creating an account ?</h3>
No. But don't worry we'll update you with current status of your order with email,sms or by phone.<br>
To view your order history you must be a registered in member.

<h3>What benefits do i have in becoming a registered member?</h3>
If you are a frequent buyer by becoming a member you can have several benefits such as:
<ul>
    <li>Fast checkout (without having to fill in billing and delivery addresses every time you checkout).</li>
    <li>View and monitor your current orders and order history.<br></li>
    <li>Review and rate our products and services.</li>
</ul>


<h3>I have signed up with <?php echo Yii::app()->name; ?> but cannot login with my password. Why ?</h3>
Two possible reasons.
First would be that you have given a wrong email address. Second would be that you have forgotten your password. <br>
You can at anytime reset your password by clicking the 'Forgot my password' link on the login page.
A temporary password will be sent to you to your registered email address.

<h3>I have registered and purchased an item. What are the three statuses listed with the order?</h3>
<ul>
    <li>Order Placed - You have placed an order and awaiting payment confirmation.</li>
    <li>Payment Confirmed - Your payment for the order has be acknowledged by the bank.</li>
    <li>Order Shipped - Your item has been handed over to the courier service to be delivered to your address.</li>
</ul>

<h3>Can I buy more than one item per order ?</h3>
Unfortunately no.<br>As our main objective is a fast and smooth checkout experience we have not implemented a shopping cart.<br>

<h3>Where do you deliver and how do i pay?</h3>
Please visit the <?php echo CHtml::link('Delivery & Pyament Options',array('/site/delivery')) ?>  page for more details on delivery and payments.

<h3>Can I send an item to some one other than my self ?</h3>
Yes. All you have to do is un-check the 'Use same address for delivery' check box and fill out the delivery address form with receipient's details.<br>
If you are a registered member, on the checkout page click on the link 'Checkout with a new address' link.